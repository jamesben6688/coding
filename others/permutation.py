"""
    时间: O(nlog(n))
    空间: O(n)

"""
def f(x):
    # 基本情况：如果序列只有一个元素，则返回该元素
    if len(x) <= 1:
        return x

    # 分割序列为奇数位置和偶数位置的子序列
    y1 = f(x[::2])  # 奇数位置元素
    y2 = f(x[1::2])  # 偶数位置元素

    # 返回拼接后的排序结果
    return y1 + y2


# 示例：求 f([1, 2, 3, 4, 5, 6])
result = f([1, 2, 3, 4, 5, 6])
print(result)  # 输出：[1, 3, 5, 2, 4, 6]
